# Ставки НДФЛ для доходов от ценных бумаг и деривативов, 2025 г.
# (ФЗ-176 от 12.07.2024, особая налоговая база по ст. 214.1 НК РФ)
# Только 13% / 15% — общая пятиступенчатая шкала (18%/20%/22%) НЕ применяется.
# Доступно только налоговым резидентам РФ.
NDFL_BRACKETS_2025: List[Tuple[float, float]] = [
    (2_400_000,    0.13),   # до 2.4 млн — 13%
    (float("inf"), 0.15),   # свыше 2.4 млн — 15%
]

# Льгота на долгосрочное владение (пп. 1 п. 1 ст. 219.1 НК РФ)
# Доступна только налоговым резидентам РФ.
LDV_YEARS_THRESHOLD: int = 3            # минимум 3 года владения
LDV_ANNUAL_DEDUCTION: float = 3_000_000.0  # 3 млн ₽ × количество полных лет


def calc_ndfl(income: float) -> float:
    """НДФЛ с дохода от ценных бумаг по ставкам 2025 г. (ФЗ-176 от 12.07.2024).

    До 2.4 млн — 13%, свыше 2.4 млн — 15% только на сумму превышения.
    Пример: 5.4 млн → 2.4 млн × 13% + 3 млн × 15% = 312 000 + 450 000 = 762 000 ₽.
    """
    if income <= 0:
        return 0.0
    tax, prev = 0.0, 0.0
    for limit, rate in NDFL_BRACKETS_2025:
        if income <= prev:
            break
        tax += (min(income, limit) - prev) * rate
        prev = limit
    return round(tax, 2)

